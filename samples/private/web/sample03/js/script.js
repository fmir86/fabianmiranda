jQuery(document).ready(function($) {

////////////////////////////////////////////////////////////////////////////////
// Zebra list striping ////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  $('ul.zebra').find('li:odd').css('background-color', 'white');

////////////////////////////////////////////////////////////////////////////////
// Sidebar: Zip Code Locator input placeholder /////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  var $locatorZip = $('#locator-zip');
  $locatorZip.val('enter zip code');

  $locatorZip.focus(function (e) {
    if ($locatorZip.val() === 'enter zip code') {
      $locatorZip.val('');
    }
  });
  $locatorZip.blur(function (e) {
    if ($.trim($locatorZip.val()) === '') {
      $locatorZip.val('enter zip code');
    }
  });
});
